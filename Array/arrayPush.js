// El metodo push() agrega uno o mas elementos al final de un array y devuelve la nueva longitud del array

const cities = ['Madrid', 'Valencia', 'Lugo'];
const total = cities.push('Bilbao', 'Santander');
console.log(cities); // [ 'Madrid', 'Valencia', 'Lugo', 'Bilbao', 'Santander' ]
console.log(total); // 5
